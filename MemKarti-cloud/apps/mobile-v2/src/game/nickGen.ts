// Генератор смешных украинских никнеймов.
// Прикалуй + назва тварини / професії = «КонспіративнаКалюжа», «ШаленийТхір».
const ADJECTIVES = [
  'Шалений', 'Хитрий', 'Сонний', 'Голодний', 'Веселий',
  'Сумний', 'Ледачий', 'Грізний', 'Кмітливий', 'Сонячний',
  'Туманний', 'Глибокий', 'Швидкий', 'Боязкий', 'Мудрий',
  'Дикий', 'Тихий', 'Гучний', 'Холодний', 'Гарячий',
  'Маленький', 'Великий', 'Сірий', 'Помаранчевий', 'Зелений',
];

const NOUNS = [
  'Кіт', 'Пес', 'Тхір', 'Лис', 'Йорж',
  'Кріт', 'Борсук', 'Бобер', 'Хом\'як', 'Хіп\'ас',
  'Котлет', 'Вареник', 'Борщ', 'Сало', 'Пиріг',
  'Гарбуз', 'Картопля', 'Кавун', 'Мандарин', 'Огірок',
  'Маг', 'Лицар', 'Хакер', 'Інженер', 'Поет',
  'Філософ', 'Сторож', 'Шеф', 'Капітан', 'Майстер',
];

export function randomNick(): string {
  const adj = ADJECTIVES[Math.floor(Math.random() * ADJECTIVES.length)];
  const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)];
  return adj + noun;
}
